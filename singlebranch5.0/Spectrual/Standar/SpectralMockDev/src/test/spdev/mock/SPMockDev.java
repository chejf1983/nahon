    private byte[] ConvertToByte(double[] tmp) {
        byte[] ret = new byte[tmp.length * 2];

        for (int i = 0; i < tmp.length; i++) {
            try {
                System.arraycopy(MyConvert.UShortToByteArray((int) tmp[i]), 0,
                        ret, i * 2, 2);
            } catch (MyConvertException ex) {

            }
        }

        return ret;
    }

    private double[] BuildSlopData() {
        try {
            double node[] = new double[nodenum];
            int integralTime = GetIntegerTime();
            for (int i = 0; i < node.length; i++) {
                node[i] = i;
                if (integralTime > 1000) {
                    node[i] *= 5000;
                } else {
                    node[i] *= integralTime * 10;
                }
            }
            return node;
        } catch (MyConvertException ex) {
            Logger.getLogger(SPMockDev.class.getName()).log(Level.SEVERE, null, ex);
        }
        return null;
    }

