    @Override
    public MIGPPacket[] ExecutorMIGPCMD(MIGPPacket cmd) throws Exception {
        if (cmd.CMD == GETMDA) {
            int mode = this.memory.GetValue(this.memory.VPA, CollectMode.addr, CollectMode.length)[0];
            if (mode == DataCollectPar.SoftMode) {
                int time = MyConvert.ByteArrayToInteger(this.memory.GetValue(this.memory.VPA, IntegralTime.addr, IntegralTime.length), 0) / 1000;
                int avr = MyConvert.ByteArrayToUShort(this.memory.GetValue(this.memory.VPA, AverageTime.addr, AverageTime.length), 0);
                TimeUnit.MILLISECONDS.sleep(time * avr);
                this.BuildData();
            } else {
                //vpa 100 位置做硬件触发位
                while (this.memory.GetValue(this.memory.VPA, 100, 1)[0] == 0x00) {
                    mode = this.memory.GetValue(this.memory.VPA, CollectMode.addr, AverageTime.length)[0];
                    //如果已经恢复软件模式，直接退出
                    if (mode == DataCollectPar.SoftMode) {
                        return new MIGPPacket[0];
                    }
                    TimeUnit.MILLISECONDS.sleep(100);
                }
                //触发数据
                this.BuildData();
                this.memory.SetValue(this.memory.VPA, 100, 1, new byte[]{0x00});
            }
        }
        return super.ExecutorMIGPCMD(cmd); //To change body of generated methods, choose Tools | Templates.
    }

    private void BuildData() throws Exception {
        int ave = MyConvert.ByteArrayToUShort(this.memory.GetValue(this.memory.VPA, AverageTime.addr, AverageTime.length),
                0);

        byte[] tmpmem;
        if (ave == 1) {
            tmpmem = ConvertToByte(this.BuidSinData());
        } else if (ave == 2) {
            tmpmem = ConvertToByte(this.GetData());
        } else {
            tmpmem = ConvertToByte(this.BuildSlopData());
        }
        this.memory.SetValue(this.memory.MDA, TestData.addr, tmpmem.length, tmpmem);
    }

    private int GetIntegerTime() throws MyConvertException {
        return MyConvert.ByteArrayToInteger(this.memory.GetValue(this.memory.VPA, IntegralTime.addr, IntegralTime.length), 0) / 1000;
    }

