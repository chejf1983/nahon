    public void SetDBIntance(EIABuilder eiaSaver) {
        this.eiaSaver = eiaSaver;
        this.CheckBox_IsServerEnable.setSelected(eiaSaver.IsServerEnable());
        this.TextField_ServerUrl.setText(eiaSaver.GetServerDB().ServerDBConnectURL);
        this.TextField_user.setText(eiaSaver.GetServerDB().s_user);
        this.TextField_password.setText(eiaSaver.GetServerDB().s_password);

        this.path_input.setText(eiaSaver.GetFileDB().FileDir);
        
        this.enableText(eiaSaver.IsServerEnable());
    }

    private void enableText(boolean value) {
        this.TextField_ServerUrl.setEnabled(value);
        this.TextField_user.setEnabled(value);
        this.TextField_password.setEnabled(value);
        this.jTextField1.setEnabled(value);
        this.path_input.setEnabled(!value);
        this.Button_SelectFile.setEnabled(!value);
    }
