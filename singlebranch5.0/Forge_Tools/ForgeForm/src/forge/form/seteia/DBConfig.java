    private void Button_ConfigActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_ConfigActionPerformed
        try {
            String url = this.TextField_ServerUrl.getText();
            String user = this.TextField_user.getText();
            String password = this.TextField_password.getText();
            this.eiaSaver.GetServerDB().SetDBConfig(url, user, password);
            this.eiaSaver.GetFileDB().SetConfig(this.path_input.getText());
            this.eiaSaver.EnableServer(this.CheckBox_IsServerEnable.isSelected());
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_Button_ConfigActionPerformed

    private void Button_SelectFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_SelectFileActionPerformed
        JFileChooser dialog = new JFileChooser(FilePath);
        dialog.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int result = dialog.showOpenDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File tmp = dialog.getSelectedFile();
            this.path_input.setText(tmp.getPath());
        }
    }//GEN-LAST:event_Button_SelectFileActionPerformed

